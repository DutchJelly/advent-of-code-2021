const tokens = [
    ['{', '}', 3],
    ['[', ']', 2],
    ['<', '>', 4],
    ['(', ')', 1]
]

const findTokenIndex = (ch) => {
    tokens.findIndex(x => x[0] === ch || x[1] === ch);
    for (let i = 0; i < tokens.length; i++) {
        if (tokens[i][0] === ch)
            return [i, true];
        else if (tokens[i][1] === ch)
            return [i, false];
    }
    return [-1, false];
}

const autoComplete = (str, iRef = { i: 0 }, expectedClosingToken) => {
    let totalScore = 0;
    for (iRef.i; iRef.i < str.length;) {
        const [tokenIndex, isOpeningToken] = findTokenIndex(str[iRef.i]);
        iRef.i++;
        if (!isOpeningToken) {
            return tokens[tokenIndex][1] === expectedClosingToken ? totalScore : NaN
        }
        const score = autoComplete(str, iRef, tokens[tokenIndex][1]);
        if (score !== 0) {
            totalScore *= 5;
            totalScore += score;
        }
    }

    //If we still expect a closing token, auto complete!
    if (expectedClosingToken) {
        const [index] = findTokenIndex(expectedClosingToken);
        totalScore *= 5;
        totalScore += tokens[index][2];
    }
    return totalScore;
};


const autoCompletionScores = [];
input.forEach(x => {
    const score = autoComplete(x);
    if (!isNaN(score)) {
        autoCompletionScores.push(score);
    }
})
autoCompletionScores.sort((a, b) => a - b);
